package com.project.shadowing;

public class MovieInfo {
    int key;
    int isLike;
    int previewId;
    int level;
    String explain;

    public MovieInfo(int key, int isLike, int previewId, int level, String explain) {
        this.key = key;
        this.isLike = isLike;
        this.previewId = previewId;
        this.level = level;
        this.explain = explain;
    }
}

